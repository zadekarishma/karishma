package array;

public class no3combine {

	public static void main(String[] args) 
	{
		int []arr=new int[] {23,86,90,76};
		String[]str= {"payal","sima","sidhi","rani"};
		for(String i:str)
		{
			System.out.println(i);
		}
		System.out.println();
		for(int j:arr)
		{
			System.out.println(j);
		}
	}

}
