
public class additionoperator 
{
	public static void main (String args[])
	{
		//additions OF animals
		int buffalo=67;
		int goat=89;
		System.out.println("no of animal="+(buffalo+goat));
		
		System.out.println("no of animal="+(buffalo+goat));
		System.out.println("no of animal="+(buffalo-goat));
		System.out.println("no of animal="+(buffalo*goat));
		System.out.println("no of animal="+(buffalo/goat));
		System.out.println("no of animal="+(buffalo%goat));
		
	}
	

}
